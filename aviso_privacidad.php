         <h2> AVISO DE PRIVACIDAD INTEGRAL</h2>
<strong>Registro de Equipos — Torneo de Robótica CECYBOT2026</strong>

<p>El Comité Organizador de CECYBOT2026, con domicilio para oír y recibir notificaciones en [DOMICILIO OFICIAL DE LA INSTITUCIÓN], es responsable del tratamiento de los datos personales que se recaben a través del sitio web oficial CECYBOT2026, los cuales serán protegidos conforme a lo dispuesto por la Ley General de Protección de Datos Personales en Posesión de Sujetos Obligados, la Ley Federal de Protección de Datos Personales en Posesión de los Particulares y demás normatividad aplicable.</p>

<h3>Datos personales recabados</h3>

<p>Los datos personales que podrán ser solicitados durante el proceso de registro de equipos incluyen:</p>

<ul>
  <li>Nombre completo de participantes y asesores.</li>
  <li>Institución educativa de procedencia.</li>
  <li>Categoría y nombre del equipo.</li>
  <li>Correo electrónico.</li>
  <li>Número telefónico de contacto.</li>
  <li>Ciudad y estado de procedencia.</li>
  <li>Información técnica del proyecto o robot participante.</li>
  <li>Fotografías, videos o material multimedia relacionados con el evento.</li>
  <li>Datos necesarios para control administrativo, acreditación y participación.</li>
</ul>

<p>En caso de que se recaben datos personales de menores de edad, estos deberán ser proporcionados bajo autorización y supervisión de madre, padre o tutor.</p>

<h3>Finalidades del tratamiento de datos</h3>

<p>Los datos personales recabados serán utilizados para las siguientes finalidades:</p>

<ul>
  <li>Registrar y validar la participación de equipos y asesores.</li>
  <li>Generar acreditaciones, constancias y documentación oficial.</li>
  <li>Dar seguimiento a la logística, programación y resultados del torneo.</li>
  <li>Establecer comunicación relacionada con el evento.</li>
  <li>Difundir resultados, actividades, fotografías y videos en medios institucionales, redes sociales, páginas oficiales y material promocional del evento.</li>
  <li>Elaborar informes estadísticos y administrativos.</li>
  <li>Garantizar la seguridad y correcto desarrollo de las actividades del torneo.</li>
</ul>

<h3>Transferencia de datos personales</h3>

<p>Los datos personales podrán ser compartidos únicamente con:</p>

<ul>
  <li>Instituciones educativas participantes.</li>
  <li>Dependencias organizadoras o patrocinadoras del evento.</li>
</ul>

<p>Autoridades competentes cuando exista requerimiento legal.</p>
<p>Plataformas tecnológicas necesarias para la operación del registro y gestión del torneo.</p>

<p>En ningún caso los datos serán comercializados o utilizados con fines distintos a los establecidos en el presente aviso de privacidad.</p>

<h3>Uso de imágenes y material audiovisual</h3>

<p>Durante el desarrollo de CECYBOT2026 podrán realizarse fotografías, grabaciones de video y transmisiones en vivo con fines informativos, académicos y de difusión institucional. La participación en el evento implica la aceptación del uso de dicho material en medios oficiales y promocionales relacionados con el torneo.</p>

<h3>Derechos ARCO</h3>

<p>Las personas titulares de los datos personales podrán ejercer sus derechos de:</p>

<ul>
  <li>Acceso</li>
  <li>Rectificación</li>
  <li>Cancelación</li>
  <li>Oposición</li>
</ul>

<p>mediante solicitud enviada al correo electrónico: jose.cervantesr@cecyteg.edu.mx o christian.lozanoh@cecyteg.edu.mx</p>

<p>La solicitud deberá contener:</p>

<ul>
  <li>Nombre del titular.</li>
  <li>Descripción clara de los datos respecto de los cuales busca ejercer algún derecho.</li>
</ul>

<p>Documento que acredite identidad</p>

<p>El presente aviso de privacidad puede ser modificado o actualizado en cualquier momento para atender reformas legales, necesidades operativas o cambios en la organización del evento. Cualquier modificación será publicada en el sitio oficial: </p>

